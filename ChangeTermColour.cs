using System;

namespace Program_Redownloader
{
    class ChangeTermColour
    {
        public static void termFGRed()
        {
            Console.ForegroundColor = ConsoleColor.Red;
        }
        public static void termFGGreen()
        {
            Console.ForegroundColor = ConsoleColor.Green;
        }
        public static void termFGBlue()
        {
            Console.ForegroundColor = ConsoleColor.Blue;
        }
        public static void termFGDarkRed()
        {
            Console.ForegroundColor = ConsoleColor.Red;
        }
        public static void termFGDarkGreen()
        {
            Console.ForegroundColor = ConsoleColor.DarkGreen;
        }
        public static void termFGDarkBlue()
        {
            Console.ForegroundColor = ConsoleColor.DarkBlue;
        }
        public static void termBGRed()
        {
            Console.BackgroundColor = ConsoleColor.Red;
        }
        public static void termBGGreen()
        {
            Console.BackgroundColor = ConsoleColor.Green;
        }
        public static void termBGBlue()
        {
            Console.BackgroundColor = ConsoleColor.Blue;
        }
        public static void termBGDarkRed()
        {
            Console.BackgroundColor = ConsoleColor.DarkRed;
        }
        public static void termBGDarkGreen()
        {
            Console.BackgroundColor = ConsoleColor.DarkGreen;
        }
        public static void termBGDarkBlue()
        {
            Console.BackgroundColor = ConsoleColor.DarkBlue;
        }

    }

}
